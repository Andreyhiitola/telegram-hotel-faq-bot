# ============= –§–£–ù–ö–¶–ò–ò –î–õ–Ø –†–ê–ë–û–¢–´ –° –ú–ï–ù–Æ =============

def create_menu_markup(page=0):
    """
    –°–æ–∑–¥–∞—ë—Ç –º–µ–Ω—é —Å –ø–∞–≥–∏–Ω–∞—Ü–∏–µ–π
    page: –Ω–æ–º–µ—Ä —Å—Ç—Ä–∞–Ω–∏—Ü—ã (–Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 0)
    """
    markup = types.InlineKeyboardMarkup()
    
    items_per_page = 4
    start_idx = page * items_per_page
    end_idx = start_idx + items_per_page
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–Ω–æ–ø–∫–∏ –¥–ª—è —ç—Ç–æ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
    for i in range(start_idx, min(end_idx, len(FAQ_KEYS))):
        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∏–Ω–¥–µ–∫—Å –≤–º–µ—Å—Ç–æ –ø–æ–ª–Ω–æ–≥–æ –Ω–∞–∑–≤–∞–Ω–∏—è –≤ callback_data
        btn_text = FAQ_KEYS[i]
        markup.add(types.InlineKeyboardButton(btn_text, callback_data=f"item_{i}"))
    
    # –ö–Ω–æ–ø–∫–∏ –Ω–∞–≤–∏–≥–∞—Ü–∏–∏
    total_pages = (len(FAQ_KEYS) + items_per_page - 1) // items_per_page
    nav_buttons = []
    
    if page > 0:
        nav_buttons.append(types.InlineKeyboardButton("‚óÄÔ∏è –ù–∞–∑–∞–¥", callback_data=f"page_{page - 1}"))
    
    nav_buttons.append(types.InlineKeyboardButton(f"{page + 1}/{total_pages}", callback_data="noop"))
    
    if end_idx < len(FAQ_KEYS):
        nav_buttons.append(types.InlineKeyboardButton("–í–ø–µ—Ä–µ–¥ ‚ñ∂Ô∏è", callback_data=f"page_{page + 1}"))
    
    if nav_buttons:
        markup.row(*nav_buttons)
    
    return markup

