        if 0 <= item_idx < len(FAQ_KEYS):
            question_key = FAQ_KEYS[item_idx]
            answer_text = FAQ[question_key]["text"]
            
            markup = types.InlineKeyboardMarkup()
            markup.add(types.InlineKeyboardButton("‚óÄÔ∏è –ù–∞–∑–∞–¥ –≤ –º–µ–Ω—é", callback_data="back_menu_0"))
            
